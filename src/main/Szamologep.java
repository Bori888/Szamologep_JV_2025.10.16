package main;

public class Szamologep {

    public static int osszead(String a, String b) {
        int num1 = Integer.parseInt(a);
        int num2 = Integer.parseInt(b);
        return num1+num2;
    }
    
    public static double osztas(String a, String b) {
        int num1 = Integer.parseInt(a);
        int num2 = Integer.parseInt(b);
        return  (double)num1/num2;
    }
    
}
